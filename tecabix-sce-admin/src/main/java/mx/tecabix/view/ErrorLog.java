    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        Thread hilo = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    LogController logController = new LogController();
                    String comentarios = jTextPaneComentarios.getText();
                    String reporte = jTextAreaDetalles.getText();
                    StringBuilder cuerpo = new StringBuilder(comentarios);
                    cuerpo.append("\n\n").append(reporte);
                    logController.post(cuerpo.toString());
                } catch (Exception ex) {
                    Logger.getLogger(ErrorLog.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        });
        hilo.start();
        this.dispose();
    }//GEN-LAST:event_jButtonAceptarActionPerformed


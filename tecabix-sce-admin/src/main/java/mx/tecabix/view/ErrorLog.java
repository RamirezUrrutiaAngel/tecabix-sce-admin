    public ErrorLog(JDialog parent, Exception ex) {
        super(parent, true);
        initComponents();
        StringBuilder sb = new StringBuilder(LocalDateTime.now().toString());
        sb.append('\n');
        if(ex.getMessage() != null){
            sb.append(ex.getMessage());
            sb.append('\n');
        }
        if(ex.getCause() != null){
            sb.append(ex.getCause());
            sb.append('\n');
        }
        if(ex.fillInStackTrace() != null){
            Throwable th = ex.fillInStackTrace();
            sb.append(th);
            sb.append('\n');
            
            if(th.getLocalizedMessage() != null){
                sb.append(th.getLocalizedMessage());
                sb.append('\n');
            }
            if(th.getMessage() != null){
                sb.append(th.getLocalizedMessage());
                sb.append('\n');
            }
        }
        
        
        StackTraceElement[] ste = ex.getStackTrace();
        if(ste != null){
            for (int i = 0; i < ste.length; i++) {
                sb.append(ste[i]);
                sb.append('\n');
                sb.append(ste[i].getClassName()).append('\n');
                sb.append(ste[i].getFileName()).append('\n');
                sb.append(ste[i].getMethodName()).append('\n');
                
            }
        }
        
        Throwable[] throwable = ex.getSuppressed();
        if(throwable != null){
            for (int i = 0; i < throwable.length; i++) {
                sb.append(throwable[i]);
                sb.append('\n');
                sb.append(throwable[i].getLocalizedMessage()).append('\n');
                sb.append(throwable[i].getMessage()).append('\n');
            }
        }
        
        jTextAreaDetalles.setText(sb.toString());
        jTextAreaDetalles.setCaretPosition(0);
        setLocationRelativeTo(parent);
        setVisible(true);
    }

